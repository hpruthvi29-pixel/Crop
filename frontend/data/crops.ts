export const CROP_CATEGORIES = ['All', 'Cereal', 'Pulse', 'Fruit', 'Cash Crop', 'Beverage'] as const
export type CropCategory = typeof CROP_CATEGORIES[number]

export function getCropById(id: string): CropInfo | undefined {
  return CROPS.find(c => c.id === id.toLowerCase())
}

export function getCropsByCategory(category: CropCategory): CropInfo[] {
  if (category === 'All') return CROPS
  return CROPS.filter(c => c.category === category)
}
